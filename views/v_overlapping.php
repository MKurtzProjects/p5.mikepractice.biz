    <p>Overlapping Set problems have two more more sets of data that partially intersect.  For example: </p>
    </em> There are 60 people at a party.  30 like jazz people.  20 like rock and jazz.  If everyone likes at least one of the two genres, how many of the people like rock only?</em>
    <p>To solve this type of problem, you'll want to divide the data into four groups:</p>
    <ul>
        <li>1. People who only love jazz</li>
        <li>2. People who only love rock</li>
        <li>3. People who love both rock and jazz</li>
        <li>4. People who don't love either</li>
    
    </br></br> 
    Distance = Rate * Time 
